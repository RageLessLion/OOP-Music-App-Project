package MyClasses;

public class Episodes {
    private String name;

    private Integer duration;

    private String description;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getDuration() {
        return duration;
    }

    public String getDescription() {
        return description;
    }

}
